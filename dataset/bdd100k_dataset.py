"""
dataset/bdd100k_dataset.py
──────────────────────────
BDD100K dataset loader for object detection.

Classes of interest: traffic cone, barrier, stop sign.
Converts BDD100K JSON annotations → YOLO-format txt labels
and exposes a PyTorch Dataset for training.

Usage:
    from dataset.bdd100k_dataset import BDD100KDataset, prepare_yolo_labels
"""

import os
import json
import shutil
import logging
from pathlib import Path
from typing import Dict, List, Optional, Tuple

import cv2
import numpy as np

try:
    import torch
    from torch.utils.data import Dataset
    from torchvision import transforms
    _TORCH_AVAILABLE = True
except ImportError:
    _TORCH_AVAILABLE = False
    # Provide a no-op base class so the file imports cleanly
    # even before torch is installed.  BDD100KDataset will raise
    # a clear error at instantiation time if torch is missing.
    class Dataset:          # type: ignore
        pass
    class transforms:       # type: ignore
        Compose = None

logger = logging.getLogger(__name__)


# ─── Class mapping ──────────────────────────────────────────────────────────
# BDD100K category name  →  our 0-indexed class id
CLASS_MAP: Dict[str, int] = {
    "traffic cone": 0,
    "barrier":      1,
    "stop sign":    2,
}
CLASS_NAMES = ["cone", "barrier", "stop_sign"]


# ─── Annotation converter ───────────────────────────────────────────────────

def bdd100k_to_yolo(
    json_path: str,
    images_root: str,
    output_labels_dir: str,
    class_map: Dict[str, int] = CLASS_MAP,
) -> int:
    """
    Convert BDD100K detection JSON → per-image YOLO .txt labels.

    Each output line:  <class_id> <cx> <cy> <w> <h>  (normalised 0-1)

    Args:
        json_path:          Path to bdd100k_labels_images_*.json
        images_root:        Directory containing the split images
        output_labels_dir:  Where to write .txt label files
        class_map:          Mapping {category_name: class_id}

    Returns:
        Number of annotation files written.
    """
    output_dir = Path(output_labels_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    with open(json_path, "r") as f:
        annotations = json.load(f)

    written = 0
    for entry in annotations:
        img_name = entry["name"]                       # e.g. "0000f77c-6257be58.jpg"
        img_path = Path(images_root) / img_name

        # Skip if image is missing
        if not img_path.exists():
            continue

        # Read image dimensions
        img = cv2.imread(str(img_path))
        if img is None:
            continue
        H, W = img.shape[:2]

        labels = entry.get("labels") or []
        yolo_lines: List[str] = []

        for lbl in labels:
            category = lbl.get("category", "").lower()
            if category not in class_map:
                continue

            cls_id = class_map[category]
            box2d  = lbl.get("box2d")
            if box2d is None:
                continue

            x1, y1, x2, y2 = box2d["x1"], box2d["y1"], box2d["x2"], box2d["y2"]
            cx = ((x1 + x2) / 2) / W
            cy = ((y1 + y2) / 2) / H
            bw = (x2 - x1) / W
            bh = (y2 - y1) / H

            # Clamp to [0, 1]
            cx, cy, bw, bh = (
                max(0.0, min(1.0, v)) for v in (cx, cy, bw, bh)
            )
            yolo_lines.append(f"{cls_id} {cx:.6f} {cy:.6f} {bw:.6f} {bh:.6f}")

        if yolo_lines:
            label_file = output_dir / (Path(img_name).stem + ".txt")
            label_file.write_text("\n".join(yolo_lines))
            written += 1

    logger.info(f"Wrote {written} label files → {output_labels_dir}")
    return written


# ─── PyTorch Dataset ────────────────────────────────────────────────────────

class BDD100KDataset(Dataset):
    """
    PyTorch Dataset for BDD100K (our 3-class subset).

    Expects YOLO-format .txt labels already generated by
    `bdd100k_to_yolo()`.  Used for custom training loops;
    for Ultralytics YOLO training, point the YAML directly at
    the data directory instead.

    Args:
        images_dir:   Directory of .jpg images
        labels_dir:   Directory of corresponding .txt labels
        img_size:     Square resize target (default 640)
        augment:      Apply random augmentations
        transforms:   Optional torchvision transform pipeline
    """

    def __init__(
        self,
        images_dir: str,
        labels_dir: str,
        img_size: int = 640,
        augment: bool = True,
        transform: Optional[transforms.Compose] = None,
    ):
        self.images_dir = Path(images_dir)
        self.labels_dir = Path(labels_dir)
        self.img_size   = img_size
        self.augment    = augment
        self.transform  = transform

        # Collect image paths that have matching labels
        self.samples: List[Path] = []
        for img_path in sorted(self.images_dir.glob("*.jpg")):
            lbl_path = self.labels_dir / (img_path.stem + ".txt")
            if lbl_path.exists():
                self.samples.append(img_path)

        logger.info(f"BDD100KDataset: {len(self.samples)} samples in {images_dir}")

    # ── dunder ────────────────────────────────────────────────────────────

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> Tuple:
        img_path = self.samples[idx]
        lbl_path = self.labels_dir / (img_path.stem + ".txt")

        # ── Load image ──────────────────────────────────────────────────
        img = cv2.imread(str(img_path))
        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        img = cv2.resize(img, (self.img_size, self.img_size))

        # ── Augmentations (training only) ───────────────────────────────
        if self.augment:
            img = self._augment(img)

        # ── To tensor ───────────────────────────────────────────────────
        if self.transform:
            img_tensor = self.transform(img)
        else:
            import torch as _torch
            img_tensor = _torch.from_numpy(img).permute(2, 0, 1).float() / 255.0

        # ── Load labels  [N, 5]  (cls, cx, cy, w, h) ───────────────────
        labels = self._load_labels(lbl_path)

        return img_tensor, labels

    # ── Helpers ───────────────────────────────────────────────────────────

    def _load_labels(self, lbl_path: Path):
        """Return [N, 5] float tensor or empty tensor if no labels."""
        if not lbl_path.exists() or lbl_path.stat().st_size == 0:
            import torch as _torch
            return _torch.zeros((0, 5), dtype=_torch.float32)

        rows = []
        for line in lbl_path.read_text().strip().splitlines():
            parts = list(map(float, line.split()))
            if len(parts) == 5:
                rows.append(parts)

        import torch as _torch
        if not rows:
            return _torch.zeros((0, 5), dtype=_torch.float32)
        return _torch.tensor(rows, dtype=_torch.float32)

    def _augment(self, img: np.ndarray) -> np.ndarray:
        """Lightweight augmentation pipeline."""
        # Horizontal flip
        if np.random.random() < 0.5:
            img = cv2.flip(img, 1)

        # Brightness / contrast jitter
        alpha = np.random.uniform(0.8, 1.2)   # contrast
        beta  = np.random.randint(-20, 20)     # brightness
        img   = np.clip(alpha * img + beta, 0, 255).astype(np.uint8)

        # Gaussian blur (occasionally)
        if np.random.random() < 0.1:
            img = cv2.GaussianBlur(img, (3, 3), 0)

        return img


# ─── YOLO data.yaml generator ───────────────────────────────────────────────

def create_yolo_data_yaml(
    dataset_root: str,
    train_images: str,
    val_images: str,
    output_yaml: str = "configs/data.yaml",
) -> str:
    """
    Write a Ultralytics-compatible data.yaml for this 3-class task.

    Args:
        dataset_root:  Absolute or relative base path
        train_images:  Path to training images (relative to dataset_root)
        val_images:    Path to validation images
        output_yaml:   Where to save the YAML

    Returns:
        Path to the written YAML.
    """
    content = f"""# Auto-generated by dataset/bdd100k_dataset.py
path: {dataset_root}
train: {train_images}
val:   {val_images}

nc: {len(CLASS_MAP)}
names: {CLASS_NAMES}
"""
    Path(output_yaml).parent.mkdir(parents=True, exist_ok=True)
    Path(output_yaml).write_text(content)
    logger.info(f"YOLO data YAML written → {output_yaml}")
    return output_yaml


